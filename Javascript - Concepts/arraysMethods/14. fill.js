// 14. Fill(): Cambia todos los elementos de un array por un valor estatico desde el indice de inicio hasta el indice final. Retorna el array modificado

const array = [1,2,3,4,5,6,7];

// llenar con 10 desde la position 3
console.log(array.fill(10,3));

// Llenar con 8 desde la position 4 hasta ala 6
console.log(array.fill(8,4,6));